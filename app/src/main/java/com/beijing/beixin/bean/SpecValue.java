
package com.beijing.beixin.bean;

public class SpecValue {

	private boolean available;
	private boolean checked;
	private String specPictUrl;
	private int specId;
	private String specPictId;
	private int specValueId;
	private String specValueNm;

	public boolean getAvailable() {
		return available;
	}

	public void setAvailable(boolean available) {
		this.available = available;
	}

	public boolean getChecked() {
		return checked;
	}

	public void setChecked(boolean checked) {
		this.checked = checked;
	}

	public String getSpecPictUrl() {
		return specPictUrl;
	}

	public void setSpecPictUrl(String specPictUrl) {
		this.specPictUrl = specPictUrl;
	}

	public int getSpecId() {
		return specId;
	}

	public void setSpecId(int specId) {
		this.specId = specId;
	}

	public String getSpecPictId() {
		return specPictId;
	}

	public void setSpecPictId(String specPictId) {
		this.specPictId = specPictId;
	}

	public int getSpecValueId() {
		return specValueId;
	}

	public void setSpecValueId(int specValueId) {
		this.specValueId = specValueId;
	}

	public String getSpecValueNm() {
		return specValueNm;
	}

	public void setSpecValueNm(String specValueNm) {
		this.specValueNm = specValueNm;
	}

}
